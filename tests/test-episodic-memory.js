import { RunnableEpisodicMemory, EmbeddingsPlugin, RunnableSequence, RunnableLambda } from '../src/index.js';

class MockSemanticEmbeddings extends EmbeddingsPlugin {
    async embedQuery(text) {
        const lower = text.toLowerCase();
        const vec = [0, 0, 0, 0];
        if (lower.includes("python") || lower.includes("coding") || lower.includes("language")) vec[0] = 1.0;
        if (lower.includes("dark mode") || lower.includes("theme") || lower.includes("ui")) vec[1] = 1.0;
        if (lower.includes("tokyo") || lower.includes("travel") || lower.includes("japan")) vec[2] = 1.0;
        if (lower.includes("coffee") || lower.includes("drink") || lower.includes("tea")) vec[3] = 1.0;
        const norm = Math.sqrt(vec.reduce((sum, v) => sum + v * v, 0));
        return norm === 0 ? [0.1, 0.1, 0.1, 0.1] : vec.map(v => v / norm);
    }
}

async function runEpisodicMemoryTests() {
    console.log("=== Testing Cross-Session Episodic Memory & Semantic Fact Indexing ===");

    const embeddings = new MockSemanticEmbeddings();
    const memory = new RunnableEpisodicMemory({
        embeddings,
        dbName: "TestEpisodicMemoryDB",
        storeName: "episodes",
        topK: 2,
        namespace: "test-user"
    });

    await memory.clear();

    // Test 1: remember() - Saving facts into long-term memory
    console.log("Test 1: Saving facts into episodic memory across categories...");
    const save1 = await memory.remember({
        fact: "User loves coding in Python and building AI agents.",
        category: "coding",
        metadata: { confidence: 0.95 }
    });
    const save2 = await memory.remember({
        fact: "User prefers dark mode UI across all applications.",
        category: "preferences"
    });
    const save3 = await memory.remember({
        fact: "User is planning a travel trip to Tokyo next spring.",
        category: "travel"
    });

    if (save1.status !== "saved" || !save1.id || save1.category !== "coding") {
        throw new Error(`Test 1 failed: Unexpected remember output ${JSON.stringify(save1)}`);
    }
    console.log("PASS: Stored 3 facts with unique episode IDs and timestamps.");

    // Test 2: recall() - Semantic similarity search
    console.log("Test 2: Recalling facts using semantic queries...");
    const codingRecall = await memory.recall("What programming language does the user like?");
    if (codingRecall.length === 0 || !codingRecall[0].fact.includes("Python")) {
        throw new Error(`Test 2 failed: Expected Python fact, got ${JSON.stringify(codingRecall)}`);
    }
    console.log("PASS: Recalled Python fact via semantic similarity match.");

    // Test 3: Category and namespace filtering
    console.log("Test 3: Recalling with category filter...");
    const travelRecall = await memory.recall({ query: "Tell me about user plans", category: "travel" });
    if (travelRecall.length !== 1 || !travelRecall[0].fact.includes("Tokyo")) {
        throw new Error(`Test 3 failed: Expected Tokyo travel fact, got ${JSON.stringify(travelRecall)}`);
    }
    console.log("PASS: Filtered recall accurately retrieved category-matched episodes.");

    // Test 4: LCEL invoke() prompt enrichment
    console.log("Test 4: Testing LCEL invoke() prompt enrichment...");
    const pipeline = RunnableSequence.from([
        memory,
        new RunnableLambda(async (enriched) => `Enriched Prompt:\nContext:\n${enriched.episodicMemory}\n\nUser Question: ${enriched.query}`)
    ]);
    const pipelineOutput = await pipeline.invoke("Any UI theme preferences?");
    if (!pipelineOutput.includes("dark mode UI")) {
        throw new Error(`Test 4 failed: LCEL pipeline did not inject dark mode memory! Got:\n${pipelineOutput}`);
    }
    console.log("PASS: LCEL pipeline successfully enriched query with semantic episodic memory.");

    // Test 5: ReAct Structured Tools (getTools)
    console.log("Test 5: Verifying ReAct structured tools generated by getTools()...");
    const [rememberTool, recallTool, forgetTool] = memory.getTools();

    if (rememberTool.name !== "remember" || recallTool.name !== "recall" || forgetTool.name !== "forget") {
        throw new Error("Test 5 failed: getTools() returned unexpected tool names.");
    }

    const toolRecallRes = await recallTool.invoke({ query: "Where is user traveling?" });
    if (!toolRecallRes.includes("Tokyo")) {
        throw new Error(`Test 5 failed: recall tool returned: ${toolRecallRes}`);
    }

    const toolRememberRes = await rememberTool.invoke({ fact: "User drinks black coffee every morning.", category: "habits" });
    if (!toolRememberRes.includes("Saved to episodic memory")) {
        throw new Error(`Test 5 failed: remember tool returned: ${toolRememberRes}`);
    }
    console.log("PASS: ReAct structured tools executed cleanly and updated long-term memory.");

    // Test 6: forget() and getAllEpisodes()
    console.log("Test 6: Verifying forget() and getAllEpisodes()...");
    const allBefore = await memory.getAllEpisodes();
    if (allBefore.length !== 4) {
        throw new Error(`Test 6 failed: Expected 4 total episodes, got ${allBefore.length}`);
    }

    await forgetTool.invoke({ id: save2.id });
    const allAfter = await memory.getAllEpisodes();
    if (allAfter.length !== 3 || allAfter.some(e => e.id === save2.id)) {
        throw new Error("Test 6 failed: Episode save2 was not forgotten accurately.");
    }
    console.log("PASS: Successfully deleted specific episode ID and verified updated episode list.");

    // Test 7: clear()
    await memory.clear();
    const allCleared = await memory.getAllEpisodes();
    if (allCleared.length !== 0) {
        throw new Error(`Test 7 failed: Expected 0 episodes after clear(), got ${allCleared.length}`);
    }
    console.log("PASS: Cleared all episodic memories successfully.");

    console.log("=== ALL EPISODIC MEMORY TESTS PASSED SUCCESSFULLY! ===");
}

runEpisodicMemoryTests().catch(err => {
    console.error("Test failed:", err);
    process.exit(1);
});
